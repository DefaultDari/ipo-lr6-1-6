# Задача:
# Написать программу, которая:
# 1. Создает случайный список из 20 значений, состоящий из 4 случайных целых чисел от -10 до 10.
# 2. Находит все уникальные комбинации пар из этих значений и выводит их в виде списка кортежей.
# 3. Запрашивает у пользователя целое число.
# 4. Вычисляет количество пар, чья сумма меньше заданного пользователем значения.

import random
randlist = []  # Пустой список, в который будем добавлять кортежи
for _ in range(20):  # Цикл, который повторится 20 раз (для 20 кортежей)
    randnumbers = []  # Пустой список, в который будем добавлять 4 случайных числа
    for _ in range(4):  # Вложенный цикл для генерации 4 чисел
        number = random.randint(-10, 10)  # Генерация случайного числа от -10 до 10
        randnumbers.append(number)  # Добавляем сгенерированное число в список randnumbers
    randlist.append(randnumbers)  # Добавляем список randnumbers в список randlist
print("Сгенерированный список кортежей:", randlist) # Печатаем сгенерированный список для проверки
uniqpairs = []  # Пустой список для хранения уникальных пар
for i in range(len(randlist)):  # Внешний цикл перебирает все элементы в randlist
    for j in range(i + 1, len(randlist)):  # Внутренний цикл перебирает элементы после текущего
        uniqpairs.append((randlist[i], randlist[j]))  # Добавляем пару в список uniqpairs
# Уникальные пары для проверки
print("Уникальные пары кортежей:") 
for pair in uniqpairs:
    print(pair)
number_input = int(input("Введите целое число: "))  # Получаем ввод пользователя, преобразуем его в целое число
pair_counter = 0  # Счетчик пар, сумма которых меньше введенного числа
for pair in uniqpairs:  # Перебираем все уникальные пары
    pair_sum = sum(pair[0]) + sum(pair[1])  # Суммируем элементы первого и второго кортежа
    if pair_sum < number_input:  # Если сумма меньше введенного числа
        pair_counter += 1  # Увеличиваем счетчик на 1
print(f"Количество пар с суммой меньше чем {number_input}: {pair_counter}") # Вывод
